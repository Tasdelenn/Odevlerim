package Odevlerim.IsmetHocadan.Odevlerim_18_;

public class _01_Odev_ {
    public static void main(String[] args) {

        // 1- # 0 ile 100 arasındaki bütün çift sayıları yazdırabileceğiniz bir kod yazınız. Not: 0 ve 100 dahildir.
        int sayac = 0;
        while (sayac <= 100) {
            System.out.println(sayac);
            sayac += 2;
        }
    }
}
